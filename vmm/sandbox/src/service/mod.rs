//! Unix-socket service API for sandbox and template management.
//!
//! Listens on `--admin-listen` (default `/run/vmm-sandboxer-admin.sock`).
//! Protocol: one JSON object per line, one request/response per connection.
//!
//! ## Sandbox actions
//!
//! ### sandbox-run — create a sandbox slot and restore from template in one step
//!
//! `snapshot_type` selects the restore mode.  Each type accepts a distinct set of fields;
//! mixing fields across types is rejected.  Memory restore mode is always the server default.
//!
//! **warm_fork**:
//! ```json
//! {"action":"sandbox-run","snapshot_type":"warm_fork","template_key":"<pool-key>"}
//! {"action":"sandbox-run","snapshot_type":"warm_fork","template_id":"<id>"}
//! {"action":"sandbox-run","snapshot_type":"warm_fork","template_key":"<pool-key>","template_id":"<id>"}
//! ```
//! - `key` — pool key; latest WarmFork template for this key is restored.
//! - `template_id` — pin to a specific template by ID.
//! - At least one of `key` or `template_id` is required. When both are provided they must match.
//! - Runs in autonomous mode: `task_id = None`, process self-starts after COMMIT.
//! - Snapshot-time targets (container_id, socket_path) are taken from the template.
//!
//! **continuation**:
//! ```json
//! {"action":"sandbox-run","snapshot_type":"continuation","pod_uid":"<uid>","generation":0}
//! ```
//! - `pod_uid` required — uniquely identifies the workload instance.
//! - `generation` optional — defaults to 0.
//! - Network identity must be migrated externally before calling this.
//!
//! Response: `{"ok":true,"sandbox_id":"<id>","template_id":"<id>"}`
//!
//! ### sandbox-destroy — stop a sandbox and release all resources
//! ```json
//! {"action":"sandbox-destroy","sandbox_id":"<id>"}
//! ```
//! Stops the VM, releases the template lease, and deletes the sandbox directory.
//! Works for both `run`-created sandboxes and containerd-managed sandboxes that are stopped.
//!
//! ### sandbox-list — list all known sandboxes
//! ```json
//! {"action":"sandbox-list"}
//! ```
//! Response: `{"ok":true,"sandboxes":[{"id":"<id>","status":"<status>","base_dir":"<dir>","template_id":"<id>","template_snapshot_type":"<type>"},...]}`
//!
//! ### sandbox-get — get details of a single sandbox
//! ```json
//! {"action":"sandbox-get","sandbox_id":"<id>"}
//! ```
//! Response includes additional fields: `lease_mode`, `memory_restore_mode`.
//!
//! ## Template actions
//!
//! ### template-create — snapshot a running sandbox into the template pool
//! ```json
//! {"action":"template-create","sandbox_id":"<id>","key":"<pool-key>"}
//! {"action":"template-create","sandbox_id":"<id>","key":"<pool-key>","snapshot_type":"warm_fork"}
//! {"action":"template-create","sandbox_id":"<id>","snapshot_type":"continuation","pod_uid":"<uid>","generation":0}
//! ```
//! - `template_id`      ignored if supplied — generated by the server.
//! - `key`     **required for WarmFork** — pool key; pods with `kuasar.io/template-key=<key>` will match.
//!                      **ignored for Continuation** — key is auto-derived from `pod_uid:generation`.
//! - `snapshot_type`    optional — `"warm_fork"` (default) or `"continuation"`.
//! - `pod_uid`          required when `snapshot_type=continuation` — uniquely identifies the workload instance.
//! - `generation`  optional when `snapshot_type=continuation` — defaults to 0.
//!
//! **WarmFork**: the sandbox process must be in ready-waiting state (blocking on the readiness socket)
//! and the pod must carry `kuasar.io/warm-fork-ready-protocol-version=v1`.
//! Restore: pod must set `kuasar.io/template-key=<key>` matching the key used here.
//!
//! **Continuation**: captures the process as-is (no ready-waiting state required). Pool key is
//! `{pod_uid}:{generation}`. The pod must set
//! `kuasar.io/pod-uid=<uid>` and optionally
//! `kuasar.io/workload-generation=<n>` for the restore path to find this template.
//! Network identity transfer to the target node is the operator's responsibility before restore.
//!
//! ### template-list / template-get — inspect available templates
//! ```json
//! {"action":"template-list"}
//! {"action":"template-get","template_id":"<id>"}
//! ```
//! Lists or returns available Environment/WarmFork templates from the pool and available
//! Continuation entries from the continuation store.
//!
//! ### template-from-bundle — (not yet implemented)
//! ```json
//! {"action":"template-from-bundle","bundle_dir":"<path>","template_id":"<id>","key":"<pool-key>"}
//! ```
//! Returns an actionable error until containerd/snapshotter integration is available.
//!
//! ### pool-status — query pool health and metrics
//! ```json
//! {"action":"pool-status"}
//! ```
//!
//! ### pool-refill — spawn background environment-VM refill tasks
//! ```json
//! {"action":"pool-refill","kind":"environment","target_depth":3}
//! ```
//! - `kind`          required; only `"environment"` is supported (WarmFork/Continuation require additional info not available here)
//! - `target_depth`  refill up to this many templates (existing + in-flight count toward the target)
//!
//! ### pool-gc — remove a single template from the pool by ID
//! ```json
//! {"action":"pool-gc","kind":"environment","template_id":"<id>"}
//! ```
//! - `kind`          `"environment"` or `"warm_fork"` (required, safety cross-check against the template's actual kind)
//! - `template_id`   ID of the template to remove (required)
//!
//! ## Response envelope
//! ```json
//! {"ok":true,...}
//! {"ok":false,"error":"<message>"}
//! ```

